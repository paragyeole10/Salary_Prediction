"""### Model Comparison using Loops"""

from sklearn.linear_model import LinearRegression
from sklearn.neighbors import KNeighborsRegressor
from sklearn.svm import SVR
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor
from sklearn.preprocessing import StandardScaler
from sklearn.pipeline import make_pipeline
from sklearn.metrics import mean_absolute_error, r2_score
import pandas as pd

# Define the models to compare
models = [
    ('Linear Regression', LinearRegression()),
    ('K-Nearest Neighbors', KNeighborsRegressor(n_neighbors=5)),
    ('Support Vector Machine', make_pipeline(StandardScaler(), SVR(kernel='rbf', C=100, gamma=0.1))),
    ('Decision Tree', DecisionTreeRegressor(random_state=42)),
    ('Random Forest', RandomForestRegressor(n_estimators=100, random_state=42))
]

results = []

# Loop through each model, train, predict, and evaluate
for name, model in models:
    print(f"\n--- {name} ---")

    # Train the model
    model.fit(X_train, y_train)

    # Make predictions
    y_pred = model.predict(X_test)

    # Evaluate the model
    mae = mean_absolute_error(y_test, y_pred)
    r2 = r2_score(y_test, y_pred)

    print(f"Mean Absolute Error: {mae}")
    print(f"R-squared: {r2}")

    results.append({'Model': name, 'MAE': mae, 'R2 Score': r2})

# Display results in a DataFrame
results_df = pd.DataFrame(results)
display(results_df.sort_values(by='R2 Score', ascending=False))

"""### Visual Comparison of Models"""

import matplotlib.pyplot as plt
import seaborn as sns

# Sort results by R2 Score for better visualization
results_df_sorted = results_df.sort_values(by='R2 Score', ascending=False)

fig, axes = plt.subplots(1, 2, figsize=(16, 6))

# Plot Mean Absolute Error
sns.barplot(x='Model', y='MAE', data=results_df_sorted, ax=axes[0], palette='viridis', hue='Model', legend=False)
axes[0].set_title('Model Comparison: Mean Absolute Error')
axes[0].set_ylabel('Mean Absolute Error')
axes[0].tick_params(axis='x', rotation=45)

# Plot R-squared Score
sns.barplot(x='Model', y='R2 Score', data=results_df_sorted, ax=axes[1], palette='magma', hue='Model', legend=False)
axes[1].set_title('Model Comparison: R-squared Score')
axes[1].set_ylabel('R-squared Score')
axes[1].tick_params(axis='x', rotation=45)
axes[1].set_ylim(0, 1) # R-squared typically ranges from 0 to 1

plt.tight_layout()
plt.show()

import pickle

# The best model (Random Forest) is the last one in the 'models' list from the comparison loop
best_model = models[4][1] # Index 4 corresponds to 'Random Forest' in the 'models' list

# Define the filename for the pickled model
model_filename = 'random_forest_model.pkl'

# Save the best model to a .pkl file
with open(model_filename, 'wb') as file:
    pickle.dump(best_model, file)

print(f"Best model (Random Forest) saved to {model_filename}")

